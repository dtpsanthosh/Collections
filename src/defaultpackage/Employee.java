package defaultpackage;

public class Employee {
	public String name;
	public int no;
	public Employee() {

	}
	public Employee(String name,int no){
		super();
		this.name=name;
		this.no=no;
	}
	public String getName() {
		return name;
	}
	public int getNo() {
		return no;
	}

}
